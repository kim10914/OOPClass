package chapter02;

public class Assigment1 {
    public static void main(String[] args) {
        System.out.println(10 / 3); // 3 -> int
        System.out.println(10.0 / 3); // 3.3333333333333335 -> double
        System.out.println((int) 10.9 + 2); // 12 -> int
        System.out.println((int) (10.9 + 2)); // 12 -> int
        System.out.println((byte) 130); // -126 -> byte
        System.out.println((char) 65); // A -> char
        System.out.println((int) 'A'); // 65 -> int
    }
}
